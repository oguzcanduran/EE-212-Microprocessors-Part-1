ORG 0H
MOV A, #00

MOV R7, #255
MOV R6, #01
MOV DPTR, #NUMBERS


BACK:
MOVC A,@A+DPTR

GREAT: MOV B, R6
CJNE A,B, NEXT1
SJMP SMALL
NEXT1:JC ASB
MOV R6,A
SJMP SMALL
ASB:
SJMP SMALL

SMALL: MOV B, R7
CJNE A,B, NEXT2
SJMP INCREMENTER
NEXT2: JNC AGB
CJNE A, #0, NOZERO
SJMP INCREMENTER
NOZERO:MOV R7, A
SJMP INCREMENTER
AGB: 
SJMP INCREMENTER

INCREMENTER:
MOV A, R1
CJNE A, #255, CONTINUE; known that array will contain at most 256 numbers
OVER: SJMP OVER
CONTINUE: INC A
MOV R1,A
SJMP BACK



